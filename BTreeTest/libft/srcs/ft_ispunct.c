#include "libft.h"

/*
** Checks if the character is a punctuation, non-alphanumeric one.
*/

int		ft_ispunct(int c)
{
	return ((c >= '!' && c <= '/') || (c >= ':' && c <= '@') \
			|| (c >= '[' && c <= '`') || (c >= '{' && c <= '~'));
}
